using System;

namespace CS_Class_Methods
{
    class Calculator  //Class defined to calculate output of methods
    {

        public static int Mult(int numb)  //Method to Multiply by 50 the inputed number
        {
            return numb * 50;
        }

        public static int Add(int numb)  //Method to add 500 to inputed number
        {
            return numb + 500;
        }
 
        public static int Subtract(int numb) //Method to deduct 100 from inputed number
        {
            return numb - 100;
        }
    }
}